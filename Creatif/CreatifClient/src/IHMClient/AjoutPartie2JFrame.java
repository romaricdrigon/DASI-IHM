    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonAjouterActionPerformed
    {//GEN-HEADEREND:event_jButtonAjouterActionPerformed
        boolean validite = true;
        String nom = jTextNom.getText();
        if(nom.isEmpty())
        {
            jTextNom.setBackground(Color.RED);
            validite = false;
        }
        else
        {
            jTextNom.setBackground(Color.WHITE);
        }
         
        float prix = 0;
        try {
            prix = Float.parseFloat(jTextPrix.getText());
            jTextPrix.setBackground(Color.WHITE);
        } catch (NumberFormatException e) {
            jTextPrix.setBackground(Color.RED);
            validite = false;
        }
        
        int dimensionX = 0;
        int dimensionY = 0;
        
        try
        {
            dimensionX = Integer.parseInt(jTextField1.getText());
            dimensionY = Integer.parseInt(jTextField2.getText());
        }
        catch (NumberFormatException e)
        {
            validite = false;
        }

        if(validite)
        {
            String caract = jTextAreaCar1.getText();
            //Récupération du bon artiste
            Artiste unArtiste = lesArtistes.get(jComboBoxArtiste.getSelectedIndex());
            Oeuvre uneOeuvre = null;
            String dimension = Integer.toString(dimensionX)+"x"+Integer.toString(dimensionY);
            if("Peinture".equals((String)jComboBox1.getSelectedItem()))
            {
                
                uneOeuvre = new Peinture(nom, caract, prix, dimension);
            } else if("Sculpture".equals((String)jComboBox1.getSelectedItem()))
            {
                uneOeuvre = new Sculpture(nom, caract, prix, dimension);
            }
         
            
            if (service.creerOeuvre(uneOeuvre, unArtiste) == 0) {
                JOptionPane.showMessageDialog(null, "Ajout correctement effectué");
                new AjoutPartie1JFrame().setVisible(true);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Ajout impossible");
            }
        }
    }//GEN-LAST:event_jButtonAjouterActionPerformed

